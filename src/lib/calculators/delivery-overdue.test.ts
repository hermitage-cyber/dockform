import { describe, expect, it } from "vitest";
import { deliveryOverdue } from "./delivery-overdue";

// Тот же набор, что в date-utils.test.ts — чтобы кейсы переноса были
// сопоставимы. 1..8 января — новогодние, 23.02 — пн-праздник.
const HOLIDAYS = new Set<string>([
  "2026-01-01", "2026-01-02", "2026-01-03", "2026-01-04",
  "2026-01-05", "2026-01-06", "2026-01-07", "2026-01-08",
  "2026-02-23",
  "2026-03-08", "2026-03-09",
]);

describe("delivery_overdue", () => {
  it("стандартный кейс: 30 дней срок, поставка позже", () => {
    const r = deliveryOverdue.run({
      "дата_договора": "2026-01-01",
      "срок_исполнения_дней": 30,
      "дата_фактического_исполнения": "2026-02-15",
    });
    expect(r["дата_дедлайна"]).toBe("31.01.2026");
    // 15.02 − 31.01 = 15 дней
    expect(r["дней_просрочки"]).toBe(15);
    // Родительный падеж без послелога — рассчитан на «в течение N (...) дней».
    expect(r["срок_исполнения_прописью"]).toBe("тридцати");
  });

  it("75 дней → «семидесяти пяти» (родительный падеж)", () => {
    const r = deliveryOverdue.run({
      "дата_договора": "2026-01-01",
      "срок_исполнения_дней": 75,
      "дата_фактического_исполнения": "2026-04-01",
    });
    expect(r["срок_исполнения_прописью"]).toBe("семидесяти пяти");
  });

  it("поставка ровно в дедлайн → 0 дней просрочки", () => {
    const r = deliveryOverdue.run({
      "дата_договора": "2026-01-01",
      "срок_исполнения_дней": 30,
      "дата_фактического_исполнения": "2026-01-31",
    });
    expect(r["дней_просрочки"]).toBe(0);
  });

  it("поставка до дедлайна → 0 дней просрочки (не отрицательное)", () => {
    const r = deliveryOverdue.run({
      "дата_договора": "2026-01-01",
      "срок_исполнения_дней": 30,
      "дата_фактического_исполнения": "2026-01-10",
    });
    expect(r["дней_просрочки"]).toBe(0);
  });

  it("21 день срок → «двадцати одного» (родительный)", () => {
    const r = deliveryOverdue.run({
      "дата_договора": "2026-01-01",
      "срок_исполнения_дней": 21,
      "дата_фактического_исполнения": "2026-01-22",
    });
    expect(r["срок_исполнения_прописью"]).toBe("двадцати одного");
  });

  it("битая дата → ошибка", () => {
    expect(() =>
      deliveryOverdue.run({
        "дата_договора": "01.01.2026",
        "срок_исполнения_дней": 30,
        "дата_фактического_исполнения": "2026-02-15",
      }),
    ).toThrow(/YYYY-MM-DD/);
  });

  it("отрицательный срок → ошибка", () => {
    expect(() =>
      deliveryOverdue.run({
        "дата_договора": "2026-01-01",
        "срок_исполнения_дней": -1,
        "дата_фактического_исполнения": "2026-02-15",
      }),
    ).toThrow(/не может быть отрицательным/);
  });

  describe("перенос дедлайна на рабочий день (ст. 193 ГК РФ)", () => {
    it("дедлайн на субботу → понедельник, дни просрочки от пн", () => {
      // 20.02.2026 пт + 1 день = 21.02 сб → 24.02 вт (23.02 — праздник).
      // Поставка 27.02 (пт) — 3 дня просрочки от 24.02, а без переноса было бы 6.
      const r = deliveryOverdue.run(
        {
          "дата_договора": "2026-02-20",
          "срок_исполнения_дней": 1,
          "дата_фактического_исполнения": "2026-02-27",
        },
        { holidays: HOLIDAYS },
      );
      expect(r["дата_дедлайна"]).toBe("24.02.2026");
      expect(r["дней_просрочки"]).toBe(3);
    });

    it("дедлайн на праздник в будний → следующий будний", () => {
      // 22.02.2026 вс + 1 день = 23.02 пн-праздник → 24.02 вт.
      const r = deliveryOverdue.run(
        {
          "дата_договора": "2026-02-22",
          "срок_исполнения_дней": 1,
          "дата_фактического_исполнения": "2026-02-24",
        },
        { holidays: HOLIDAYS },
      );
      expect(r["дата_дедлайна"]).toBe("24.02.2026");
      expect(r["дней_просрочки"]).toBe(0);
    });

    it("новогодняя цепочка: 31.12.2025 + 1 день → 09.01.2026 (пт)", () => {
      const r = deliveryOverdue.run(
        {
          "дата_договора": "2025-12-31",
          "срок_исполнения_дней": 1,
          "дата_фактического_исполнения": "2026-01-09",
        },
        { holidays: HOLIDAYS },
      );
      // 01.01..08.01 — праздники, 09.01.2026 — пт ← первый рабочий.
      expect(r["дата_дедлайна"]).toBe("09.01.2026");
      expect(r["дней_просрочки"]).toBe(0);
    });

    it("фолбэк: ctx без holidays → переноса нет (старое поведение)", () => {
      // Тот же ввод, что и в первом кейсе с переносом, но без holidays.
      const r = deliveryOverdue.run({
        "дата_договора": "2026-02-20",
        "срок_исполнения_дней": 1,
        "дата_фактического_исполнения": "2026-02-27",
      });
      expect(r["дата_дедлайна"]).toBe("21.02.2026"); // сб без переноса
      expect(r["дней_просрочки"]).toBe(6);
    });

    it("рабочий дедлайн не двигается при включённом holidays", () => {
      const r = deliveryOverdue.run(
        {
          "дата_договора": "2026-02-19",
          "срок_исполнения_дней": 1,
          "дата_фактического_исполнения": "2026-02-25",
        },
        { holidays: HOLIDAYS },
      );
      // 20.02.2026 — пт, рабочий, без сдвига.
      expect(r["дата_дедлайна"]).toBe("20.02.2026");
    });
  });
});
