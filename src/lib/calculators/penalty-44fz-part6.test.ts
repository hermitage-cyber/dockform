import { describe, expect, it } from "vitest";
import { penalty44FzPart6 } from "./penalty-44fz-part6";

describe("penalty_44fz_part6", () => {
  it("реалистичный кейс: 1 000 000 ₽, 30 дней, 21%", () => {
    const r = penalty44FzPart6.run({
      "сумма_контракта": 1_000_000,
      "дней_просрочки": 30,
      "ставка_цб": 21,
    });
    // (21 / 100 / 300) × 1 000 000 × 30 = 21 000 — формат русской локали.
    expect(r["сумма_неустойки"]).toBe("21 000,00");
    expect(r["расчёт_подробно"]).toContain("÷ 300 =");
  });

  it("кейс из образца претензии: 655 713,48 ₽, 101 день, 14,5% → разбивка", () => {
    const r = penalty44FzPart6.run({
      "сумма_контракта": 655_713.48,
      "дней_просрочки": 101,
      "ставка_цб": 14.5,
    });
    // (14,5 / 100 / 300) × 655 713,48 × 101 = 32 009,75…
    // toLocaleString ru-RU разделяет тысячи неразрывным пробелом (U+00A0);
    // нормализуем в обычный, чтобы сравнение не зависело от невидимого символа.
    const nb = (v: unknown) => String(v).replace(/\u00a0/g, " ");
    expect(nb(r["сумма_неустойки"])).toBe("32 009,75");
    expect(nb(r["сумма_неустойки_руб"])).toBe("32 009");
    expect(nb(r["сумма_неустойки_коп"])).toBe("75");
    expect(nb(r["сумма_неустойки_прописью"])).toBe("Тридцать две тысячи девять");
    expect(nb(r["сумма_базы_формат"])).toBe("655 713,48");
    expect(nb(r["ставка_формат"])).toBe("14,5");
  });

  it("копейки <10 дополняются нулём слева", () => {
    // подбираем вход, дающий ровно X,0Y: 300 ₽, 100 дней, 1% → 1,00; сместим
    const r = penalty44FzPart6.run({
      "сумма_контракта": 100,
      "дней_просрочки": 9,
      "ставка_цб": 1,
    });
    // (1/100/300) × 100 × 9 = 0,03
    expect(r["сумма_неустойки_руб"]).toBe("0");
    expect(r["сумма_неустойки_коп"]).toBe("03");
    expect(r["сумма_неустойки_прописью"]).toBe("Ноль");
  });

  it("ноль дней просрочки → ноль неустойки", () => {
    const r = penalty44FzPart6.run({
      "сумма_контракта": 1_000_000,
      "дней_просрочки": 0,
      "ставка_цб": 21,
    });
    expect(r["сумма_неустойки"]).toBe("0,00");
  });

  it("отрицательные значения бросают ошибку", () => {
    expect(() =>
      penalty44FzPart6.run({
        "сумма_контракта": -1,
        "дней_просрочки": 10,
        "ставка_цб": 21,
      }),
    ).toThrow(/отрицательн/);
  });

  it("нечисловые входы бросают понятную ошибку", () => {
    expect(() =>
      penalty44FzPart6.run({
        "сумма_контракта": "не_число",
        "дней_просрочки": 10,
        "ставка_цб": 21,
      }),
    ).toThrow(/должны быть числами/);
  });
});
