import { describe, expect, it } from "vitest";
import { penalty44FzPartial } from "./penalty-44fz-partial";

const nb = (v: unknown) => String(v).replace(/\u00a0/g, " ");

describe("penalty_44fz_partial (обобщённый, 1–4 поставки)", () => {
  it("эталон 2 поставки: (1 310 532,30 − (631 726,36 + 638 080,34)) × 15% × 20 дн", () => {
    const r = penalty44FzPartial.run({
      "цена_контракта": 1_310_532.3,
      "поставка1_сумма": 631_726.36,
      "поставка2_сумма": 638_080.34,
      "дней_просрочки": 20,
      "ставка_цб": 15,
    });
    expect(nb(r["база_формат"])).toBe("40 725,60");
    expect(nb(r["сумма_неустойки"])).toBe("407,26");
    expect(nb(r["сумма_неустойки_руб"])).toBe("407");
    expect(nb(r["сумма_неустойки_коп"])).toBe("26");
    expect(nb(r["сумма_неустойки_прописью"])).toBe("Четыреста семь");
    expect(nb(r["поставка1_формат"])).toBe("631 726,36");
    expect(nb(r["поставка2_формат"])).toBe("638 080,34");
  });

  it("одна поставка: 2..4 отсутствуют → трактуются как 0", () => {
    const r = penalty44FzPartial.run({
      "цена_контракта": 1_000_000,
      "поставка1_сумма": 600_000,
      "дней_просрочки": 10,
      "ставка_цб": 21,
    });
    // база = 400 000; пеня = (21/100/300) × 400 000 × 10 = 2 800
    expect(nb(r["база_формат"])).toBe("400 000,00");
    expect(nb(r["сумма_неустойки"])).toBe("2 800,00");
    expect(nb(r["поставка1_формат"])).toBe("600 000,00");
    expect(nb(r["поставка2_формат"])).toBe("0,00"); // не маппится в YAML для N=1
  });

  it("четыре поставки складываются", () => {
    const r = penalty44FzPartial.run({
      "цена_контракта": 1_000_000,
      "поставка1_сумма": 100_000,
      "поставка2_сумма": 200_000,
      "поставка3_сумма": 300_000,
      "поставка4_сумма": 350_000,
      "дней_просрочки": 5,
      "ставка_цб": 20,
    });
    // исполнено 950 000; база 50 000; пеня = (20/100/300) × 50 000 × 5 = 166,67
    expect(nb(r["база_формат"])).toBe("50 000,00");
    expect(nb(r["сумма_неустойки"])).toBe("166,67");
    expect(nb(r["поставка4_формат"])).toBe("350 000,00");
  });

  it("исполнено больше цены → понятная ошибка", () => {
    expect(() =>
      penalty44FzPartial.run({
        "цена_контракта": 100,
        "поставка1_сумма": 80,
        "поставка2_сумма": 80,
        "дней_просрочки": 10,
        "ставка_цб": 15,
      }),
    ).toThrow(/больше цены/);
  });
});
