import { describe, expect, it } from "vitest";
import { overdueFromDeadline } from "./overdue-from-deadline";

// Тот же набор праздников, что в date-utils.test.ts и delivery-overdue.test.ts.
const HOLIDAYS = new Set<string>([
  "2026-01-01", "2026-01-02", "2026-01-03", "2026-01-04",
  "2026-01-05", "2026-01-06", "2026-01-07", "2026-01-08",
  "2026-02-23",
  "2026-03-08", "2026-03-09",
]);

describe("overdue_from_deadline", () => {
  it("дедлайн датой, поставка позже → дни просрочки + дедлайн в дд.мм.гггг", () => {
    const r = overdueFromDeadline.run({
      "дата_дедлайна": "2026-01-31",
      "дата_фактического_исполнения": "2026-02-15",
    });
    expect(r["дата_дедлайна_формат"]).toBe("31.01.2026");
    expect(r["дней_просрочки"]).toBe(15);
  });

  it("поставка ровно в дедлайн → 0 дней просрочки", () => {
    const r = overdueFromDeadline.run({
      "дата_дедлайна": "2026-01-31",
      "дата_фактического_исполнения": "2026-01-31",
    });
    expect(r["дней_просрочки"]).toBe(0);
  });

  it("поставка до дедлайна → 0 дней (не отрицательное)", () => {
    const r = overdueFromDeadline.run({
      "дата_дедлайна": "2026-01-31",
      "дата_фактического_исполнения": "2026-01-10",
    });
    expect(r["дней_просрочки"]).toBe(0);
  });

  it("битая дата → ошибка", () => {
    expect(() =>
      overdueFromDeadline.run({
        "дата_дедлайна": "31.01.2026",
        "дата_фактического_исполнения": "2026-02-15",
      }),
    ).toThrow(/YYYY-MM-DD/);
  });

  describe("перенос дедлайна на рабочий день (ст. 193 ГК РФ)", () => {
    it("дедлайн-датой на субботу → понедельник, дни просрочки от пн", () => {
      // 21.02.2026 сб → 24.02 вт (22.02 вс, 23.02 праздник).
      // Поставка 27.02 — 3 дня просрочки (вместо 6 без переноса).
      const r = overdueFromDeadline.run(
        {
          "дата_дедлайна": "2026-02-21",
          "дата_фактического_исполнения": "2026-02-27",
        },
        { holidays: HOLIDAYS },
      );
      expect(r["дата_дедлайна_формат"]).toBe("24.02.2026");
      expect(r["дней_просрочки"]).toBe(3);
    });

    it("дедлайн-датой на праздник в будний → следующий будний", () => {
      // 23.02.2026 — пн-праздник → 24.02 вт.
      const r = overdueFromDeadline.run(
        {
          "дата_дедлайна": "2026-02-23",
          "дата_фактического_исполнения": "2026-02-24",
        },
        { holidays: HOLIDAYS },
      );
      expect(r["дата_дедлайна_формат"]).toBe("24.02.2026");
      expect(r["дней_просрочки"]).toBe(0);
    });

    it("фолбэк: ctx без holidays → переноса нет (старое поведение)", () => {
      const r = overdueFromDeadline.run({
        "дата_дедлайна": "2026-02-21",
        "дата_фактического_исполнения": "2026-02-27",
      });
      expect(r["дата_дедлайна_формат"]).toBe("21.02.2026"); // сб без переноса
      expect(r["дней_просрочки"]).toBe(6);
    });

    it("рабочий дедлайн не двигается при включённом holidays", () => {
      const r = overdueFromDeadline.run(
        {
          "дата_дедлайна": "2026-02-20", // пт
          "дата_фактического_исполнения": "2026-02-25",
        },
        { holidays: HOLIDAYS },
      );
      expect(r["дата_дедлайна_формат"]).toBe("20.02.2026");
    });
  });
});
